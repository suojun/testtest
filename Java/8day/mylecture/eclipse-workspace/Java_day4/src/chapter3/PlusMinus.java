package chapter3;

public class PlusMinus {

	public static void main(String[] args) {
		int num  = 10;
		
		System.out.println(7%3);
		System.out.println(4*3);
		System.out.println(7/3);
		
		num = -num;
		System.out.println(num);
		
		//byte
		byte a = 1;
		System.out.println(a);
		System.out.println(-a);

	}

}
